DROP TABLE IF EXISTS discs;

CREATE TABLE discs (
  id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
  name TEXT NOT NULL,
  brand TEXT NOT NULL,
  speed NUMERIC(3, 1) NOT NULL,
  glide NUMERIC(2, 1) NOT NULL,
  turn NUMERIC(2, 1) NOT NULL,
  fade NUMERIC(2, 1) NOT NULL
);

DROP TABLE IF EXISTS users;

CREATE TABLE users (
  id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
  first_name TEXT NOT NULL,
  last_name TEXT NOT NULL,
  username TEXT NOT NULL,
  email TEXT NOT NULL,
  password TEXT NOT NULL,
  date_created TIMESTAMPTZ DEFAULT now()
);

DROP TABLE IF EXISTS user_bag_discs;

CREATE TABLE user_bag_discs (
  id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
  user_id INTEGER REFERENCES users(id) NOT NULL,
  disc_id INTEGER REFERENCES discs(id) NOT NULL
);

DROP TABLE IF EXISTS scorecards;


CREATE TABLE scorecards (
